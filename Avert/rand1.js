    let isDragging = false;
    let startY = 0;
    let startThumbTop = 0;
    let currentThumbTop = 0;

  
    const updateThumbPosition = () => {
        if (isDragging) return;

        const currentScroll = gallery.scrollTop;
        const totalScrollHeight = gallery.scrollHeight;
        const halfScrollHeight = totalScrollHeight / 2;

        if (currentScroll >= halfScrollHeight) {
            gallery.scrollTop = currentScroll - halfScrollHeight;
        } else if (currentScroll <= 0) {
            gallery.scrollTop = halfScrollHeight + currentScroll;
        }

        const maxThumbTop = track.clientHeight - thumb.clientHeight;
        const scrollPercent = gallery.scrollTop / (halfScrollHeight - gallery.clientHeight);
        currentThumbTop = Math.max(0, Math.min(scrollPercent * maxThumbTop, maxThumbTop));
        
        thumb.style.transform = `translateY(${currentThumbTop}px)`;
    };

    gallery.addEventListener('scroll', updateThumbPosition);

   
    thumb.addEventListener('mousedown', (e) => {
        isDragging = true;
        startY = e.clientY;
        startThumbTop = currentThumbTop;
        document.body.style.userSelect = 'none';
    });

    document.addEventListener('mousemove', (e) => {
        if (!isDragging) return;

        const deltaY = e.clientY - startY;
        const maxThumbTop = track.clientHeight - thumb.clientHeight;
        currentThumbTop = Math.max(0, Math.min(startThumbTop + deltaY, maxThumbTop));

        thumb.style.transform = `translateY(${currentThumbTop}px)`;

        const halfScrollHeight = gallery.scrollHeight / 2;
        const scrollPercent = currentThumbTop / maxThumbTop;
        gallery.scrollTop = scrollPercent * (halfScrollHeight - gallery.clientHeight);
    });

    document.addEventListener('mouseup', () => {
        if (isDragging) {
            isDragging = false;
            document.body.style.userSelect = '';
        }
    });

    
